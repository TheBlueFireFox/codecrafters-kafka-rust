use tempfile::{tempdir, TempDir};

pub fn write_files() -> TempDir {
    let tmp_dir = tempdir().expect("able to create a tmp dir");

    for file in FILES {
        let path = tmp_dir.path().join(file.path);

        let parent = path.parent().expect("has parent");

        if !parent.exists() {
            std::fs::create_dir_all(parent).expect("able to create path");
        }

        std::fs::write(path, file.data).expect("able to write to cluster");
    }
    tmp_dir
}

